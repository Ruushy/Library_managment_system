     public void  UpdateStudent() throws Exception{
        int id = Integer.parseInt(txt_stid.getText());
        String sName = txt_stName.getText();
        String course = txt_stCourse.getSelectedItem().toString();
         String Ubranch = txt_stBranch.getSelectedItem().toString();

        
 
         try {
              Class.forName("com.mysql.cj.jdbc.Driver");
              con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library_ms","root","arkani987794");
        
               PreparedStatement ps = con.prepareStatement("UPDATE student_details set  id=?, student_name=?, course=?, branch=?  WHERE id='"+id+"'");
                ps.setInt(1, id);
                ps.setString(2, sName);
                ps.setString(3, course);
                ps.setString(4, Ubranch);
                
                ps.executeUpdate();

                JOptionPane.showMessageDialog(this, "Student updated successfully");
              
         } catch (HeadlessException | ClassNotFoundException | SQLException e) {
              JOptionPane.showMessageDialog(this, e);

         }
    }
     
     
