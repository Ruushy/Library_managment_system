    private void tbl_studentDetailsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_studentDetailsMouseClicked
        // TODO add your handling code here:
        int rowNo = tbl_studentDetails.getSelectedRow();
        TableModel modell = tbl_studentDetails.getModel();
        
        txt_stid.setText(modell.getValueAt(rowNo, 0).toString());
        txt_stName.setText(modell.getValueAt(rowNo, 1).toString());
        txt_stCourse.setSelectedItem(modell.getValueAt(rowNo, 2).toString());
        txt_stBranch.setSelectedItem(modell.getValueAt(rowNo, 3).toString());
    }//GEN-LAST:event_tbl_studentDetailsMouseClicked

